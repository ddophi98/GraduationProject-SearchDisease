import SwiftUI

extension Color {
    static var systemGreen: Color? {
        return Color("SystemGreen")
    }
    
    static var systemOrange: Color? {
        return Color("SystemOrange")
    }
    
    static var systemBlue: Color? {
        return Color("SystemBlue")
    }
}
